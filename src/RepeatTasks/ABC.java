package RepeatTasks;

public class ABC {
    public static void main(String[] args) {

        for(char ch='A'; ch<='Z'; ch++){

            System.out.print(ch+" ");
        }

        System.out.println();

        for(char ch='z'; ch>='a'; ch--) {

            System.out.print(ch + " ");
        }

        int num = 67;

        int sumofnums=0;

       for(int i=1;i<=num; i++ ){

           sumofnums+=i;

       }

        System.out.println();

        System.out.println(sumofnums);

















    }






}
