package day10_Switch_Scanner;

public class SwitchStatements_Practize2 {
    public static void main(String[] args) {


        int num = 0; // 2+10-5=7

        switch (3) {
            case 1:
                num += 3;
                break;
            case 2:
                num += 2;


            case 3:
                num += 10;

            default:
                num -= 5;

        }
            System.out.println(num);
















    }


}
