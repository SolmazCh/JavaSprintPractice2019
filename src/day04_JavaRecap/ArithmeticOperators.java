package day04_JavaRecap;

public class ArithmeticOperators {


    public static void main(String[] args) {


        System.out.println(9.5+3); // 12.5
        System.out.println(20.5f-10); // 10.5
        System.out.println(3*5.5); // 16.5
        System.out.println(10/4); // 2 int only accept whole numbers
        System.out.println(10.0/4); // 2.5
        System.out.println(10/4.0); // 2.5


        double a1 = 10/4;
        System.out.println(a1); // 2.0


        double a2 =10L/4;
        System.out.println(a2); // 2.0
        double a3 = 10f/4;
        System.out.println(a3); //2.5

        double a4 = 10/4.0;
        System.out.println(a4); // 2.5

        System.out.println(a1+a2);

        //10 % 4 ==>2;
        System.out.println(30%3); // the is no remainder means =0
        System.out.println( 10 % 4);
        System.out.println(30%3);
















    }







}
